package com.comviva.hceservice.common;

/**
 * Type of schemes.
 */
public enum SchemeType {
    /**
     * All supported scheme types
     */
    ALL,

    /**
     * No Scheme.
     */
    NONE,

    /**
     * Scheme type VISA Only
     */
    VISA,

    /**
     * Scheme type MasterCard Only
     */
    MASTERCARD

}
